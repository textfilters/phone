import { lowerNfkc } from "@textfilters/core";

export const ASCII_DIGIT_RE = /^[0-9]$/u;
const DECIMAL_ZERO_CODE_POINTS = [
  0x30, 0x660, 0x6f0, 0x7c0, 0x966, 0x9e6, 0xa66, 0xae6, 0xb66, 0xbe6, 0xc66,
  0xce6, 0xd66, 0xde6, 0xe50, 0xed0, 0xf20, 0x1040, 0x1090, 0x17e0, 0x1810,
  0x1946, 0x19d0, 0x1a80, 0x1a90, 0x1b50, 0x1bb0, 0x1c40, 0x1c50, 0xa620,
  0xa8d0, 0xa900, 0xa9d0, 0xa9f0, 0xaa50, 0xabf0, 0xff10, 0x104a0, 0x10d30,
  0x10d40, 0x11066, 0x110f0, 0x11136, 0x111d0, 0x112f0, 0x11450, 0x114d0,
  0x11650, 0x116c0, 0x116d0, 0x11730, 0x118e0, 0x11950, 0x11bf0, 0x11c50,
  0x116da, 0x11d50, 0x11da0, 0x11de0, 0x11f50, 0x16130, 0x16a60, 0x16ac0,
  0x16b50, 0x16d70, 0x1ccf0, 0x1d7ce, 0x1d7d8, 0x1d7e2, 0x1d7ec, 0x1d7f6,
  0x1e140, 0x1e2f0, 0x1e4f0, 0x1e5f1, 0x1e950, 0x1fbf0,
] as const;

export const toAsciiDigit = (ch: string): string | null => {
  const code = ch.codePointAt(0);
  if (code === undefined) return null;
  for (const zero of DECIMAL_ZERO_CODE_POINTS) {
    const value = code - zero;
    if (value >= 0 && value <= 9) return String(value);
  }
  return null;
};

export const toRawChar = (ch: string): string => {
  const normalizedChars = Array.from(lowerNfkc(ch));
  // Multi-code-point NFKC expansions are symbols, not phone digits or letters.
  const normalized = normalizedChars.length === 1 ? normalizedChars[0] : ch;
  return toAsciiDigit(normalized) ?? normalized;
};
